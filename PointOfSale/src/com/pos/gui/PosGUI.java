    private void addToCartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartButtonActionPerformed
        try{
            if (!stockTable.getSelectionModel().isSelectionEmpty()) {
                int RN = (int) Double.parseDouble(ReceiptNoField.getText());
                int row = stockTable.getSelectedRow();
                String productID = (stockTable.getModel().getValueAt(row,0).toString());
                String article = articleField.getText();
                String description = descriptionField.getText();
                int productQuantity = (int) Double.parseDouble(quantityHandField.getText());
                int quantityToBuy =(int)Double.parseDouble(quantityBuyField.getText());
                int price = (int)Double.parseDouble(priceField.getText());
                int total;
                int disc = (int)Double.parseDouble(discField.getText());
                if (discField.getText().isEmpty()) {
                    total=quantityToBuy*price;
                }
                else{
                    total=quantityToBuy*price;
                    total-=((total/100)*disc);
                }   
                String date = dateField.getText();
                String transaction = transactionField.getText();
                if (pos.addToCart(RN,productID,article,description,price,quantityToBuy,disc,total,date,transaction)) {
                    updateCartTable();
                    productQuantity-=quantityToBuy;
                    pos.updateProductQuantity(productID,productQuantity);
                    JOptionPane.showMessageDialog(null, "Product Added Successfully!");
                    total=pos.calculateTotal();
                    totalField.setText(String.valueOf(total));
                    emptyTextFields();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Product is Already Entered!\nPlease Remove it First in order to Enter New","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"All Fields Need to be filled!","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Quantity and Disc should be an Integer Value! and cannot be NULL","Warning!",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_addToCartButtonActionPerformed

