    private void paymentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paymentButtonActionPerformed
        try{
        int total = (int)Double.parseDouble(totalField.getText());
        int paid = (int)Double.parseDouble(paymentField.getText());
        if (paid >= total) {
            int change = paid-total;
            changeField.setText(String.valueOf(change));
            pos.makeSale();
            Inventory inventory = new Inventory();
            inventory.addStockOut();
            printReceiptButton.setEnabled(true);
            pos.updateReceipt(ReceiptNoField.getText(),dateField.getText(),total);
            updateReceiptNumber();
        }
        else{
            JOptionPane.showMessageDialog(null,"Amount is not Enough!","Error!",JOptionPane.ERROR_MESSAGE);
        }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please Enter Amount","Error!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_paymentButtonActionPerformed

