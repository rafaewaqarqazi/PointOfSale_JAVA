    private void groupSaleTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_groupSaleTableMouseClicked
        try{
            String filter = groupFilterBox.getSelectedItem().toString();
        if (filter.equals("All")) {
            if (!groupSaleTable.getSelectionModel().isSelectionEmpty()) {
                int row = groupSaleTable.getSelectedRow();
                String receiptNo = (groupSaleTable.getModel().getValueAt(row,0).toString());
                groupSaleDetailsTable.setModel(DbUtils.resultSetToTableModel(inventory.getAllResultForGroupSaleDetailsTable(receiptNo)));
            }
        }
        else if (filter.equals("Daily")) {
                int row = groupSaleTable.getSelectedRow();
                String date = (groupSaleTable.getModel().getValueAt(row,0).toString());
                groupSaleDetailsTable.setModel(DbUtils.resultSetToTableModel(inventory.getDailyResultForGroupSaleDetailsTable(date)));
        }
        else if (filter.equals("LastWeek")) {
                int row = groupSaleTable.getSelectedRow();
                String year = (groupSaleTable.getModel().getValueAt(row,0).toString());
                String week = (groupSaleTable.getModel().getValueAt(row,1).toString());
                groupSaleDetailsTable.setModel(DbUtils.resultSetToTableModel(inventory.getWeeklyResultForGroupSaleDetailsTable(week,year)));
        }
        else{
                int row = groupSaleTable.getSelectedRow();
                String year = (groupSaleTable.getModel().getValueAt(row,0).toString());
                String month = (groupSaleTable.getModel().getValueAt(row,1).toString());
                groupSaleDetailsTable.setModel(DbUtils.resultSetToTableModel(inventory.getMonthlyResultForGroupSaleDetailsTable(year,month)));
        }
        }
        catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Please Select Row!","Warning",JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_groupSaleTableMouseClicked

