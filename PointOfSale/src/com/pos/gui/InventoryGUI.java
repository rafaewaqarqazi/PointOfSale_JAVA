    private void updateCategoryBox(){
        try {
            AddProduct add = new AddProduct();
            result =add.getCategoryNameResult();
            while(result.next()){
                categoryBox.addItem(result.getString(1));
                filterBox.addItem(result.getString(1));
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
       
    }
    private void updateUnitBox(){
        try {
            AddProduct add = new AddProduct();
            result = add.getUnitNameResult();
            while(result.next()){
                unitBox.addItem(result.getString(1));
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
