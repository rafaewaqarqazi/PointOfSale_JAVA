    private void inventoryTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inventoryTableMouseClicked
        try {
            setEditableFalse();
            int row = inventoryTable.getSelectedRow();
            String productID = (inventoryTable.getModel().getValueAt(row,0).toString());
            result = inventory.getResultFromInventoryTable(productID);
            while(result.next()){
                articleField.setText(result.getString(2));
                quantityField.setText(result.getString(5));
                buyingPriceField.setText(result.getString(7));
                categoryBox.setSelectedItem(result.getString(3));
                unitBox.setSelectedItem(result.getString(6));
                sellingPriceField.setText(result.getString(8));
                descriptionField.setText(result.getString(4));
                dateRegisteredField.setText(result.getString(9));
                dateUpdatedField.setText(result.getString(10));
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_inventoryTableMouseClicked

