    private void calculateProfitLossButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateProfitLossButtonActionPerformed
        try {
            long expense = 0,totalReturn = 0,expectedReturn = 0;
            result = inventory.calculateProfit();
            while(result.next()){
                expense =Long.parseLong(result.getString(1));
                expectedReturn = Long.parseLong(result.getString(2));
                totalReturn = Long.parseLong(result.getString(3));
            }
            expenseField.setText(String.valueOf(expense));
            actualReturnField.setText(String.valueOf(totalReturn));
            expectedReturnField.setText(String.valueOf(expectedReturn));
            if (expense<totalReturn) {
                profitField.setText(String.valueOf(totalReturn-expense));
                lossField.setText("0");
            }
            else{
                profitField.setText("0");
                lossField.setText(String.valueOf(expense-totalReturn));
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_calculateProfitLossButtonActionPerformed


